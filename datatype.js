//Primitive Data types

let a = 10 
console.log(typeof(a)); //number  
 
let str = "Hello" 
console.log(typeof(str)) // string

let avg_mark = 85.8 
console.log(typeof(avg_mark)) // number 

let bool = true 
console.log(typeof(bool)) // boolean 

let val; 
console.log(typeof(val)) // Undefined 
// In JS if the variable is defined but the value is not assigned then by default it takes undefined as a value to that variable.


